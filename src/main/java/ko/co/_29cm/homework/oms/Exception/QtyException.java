package ko.co._29cm.homework.oms.Exception;

public class QtyException extends Exception{
    public QtyException() {
        super();
    }
    public QtyException(String message, Throwable cause) {
        super(message, cause);
    }
    public QtyException(String message) {
        super(message);
    }
    public QtyException(Throwable cause) {
        super(cause);
    }
}
